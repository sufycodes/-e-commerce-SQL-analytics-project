-- SECTION 12: Update and Delete (DML)

-- After orders are delivered, subtract sold quantities from stock
UPDATE products p
JOIN (
    SELECT product_id, SUM(quantity) AS total_sold
    FROM order_items oi
    JOIN orders o ON oi.order_id = o.order_id
    WHERE o.status = 'Delivered'
    GROUP BY product_id
) AS sales ON p.product_id = sales.product_id
SET p.stock_qty = p.stock_qty - sales.total_sold
WHERE p.stock_qty >= sales.total_sold;


-- Mark any product with no remaining stock as inactive
UPDATE products
SET is_active = 0
WHERE stock_qty = 0;


-- Clean up old cancelled orders 
DELETE FROM orders
WHERE status = 'Cancelled'
  AND order_date < DATE_SUB(NOW(), INTERVAL 1 YEAR);

-- SECTION 13: Capstone Query
-- Putting it all together: CTEs + window functions + CASE WHEN

WITH

-- Step 1: Total up how much each customer has spent and how many orders they made
customer_stats AS (
    SELECT
        cu.customer_id,
        CONCAT(cu.first_name, ' ', cu.last_name) AS customer_name,
        cu.city,
        cu.segment,
        COUNT(DISTINCT o.order_id)               AS total_orders,
        ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS lifetime_value
    FROM customers cu
    JOIN orders      o  ON cu.customer_id = o.customer_id
    JOIN order_items oi ON o.order_id     = oi.order_id
    WHERE o.status != 'Cancelled'
    GROUP BY cu.customer_id, customer_name, cu.city, cu.segment
),

-- Step 2: Apply rankings and quartiles using window functions
ranked_customers AS (
    SELECT
        *,
        RANK()   OVER (ORDER BY lifetime_value DESC)                          AS overall_rank,
        RANK()   OVER (PARTITION BY city ORDER BY lifetime_value DESC)        AS city_rank,
        NTILE(4) OVER (ORDER BY lifetime_value DESC)                          AS spending_quartile,
        ROUND(lifetime_value / SUM(lifetime_value) OVER () * 100, 2)         AS revenue_share_pct
    FROM customer_stats
)

-- Step 3: Final output with a readable tier label for each customer
SELECT
    overall_rank,
    customer_name,
    city,
    segment,
    total_orders,
    lifetime_value,
    city_rank,
    revenue_share_pct,
    spending_quartile,
    CASE spending_quartile
        WHEN 1 THEN 'Platinum - Top 25%'
        WHEN 2 THEN 'Gold - Upper Mid'
        WHEN 3 THEN 'Silver - Lower Mid'
        WHEN 4 THEN 'Bronze - Bottom 25%'
    END AS customer_tier
FROM ranked_customers
ORDER BY overall_rank;


