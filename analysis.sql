-- SECTION 4: Aggregation and Grouping

-- Q9. How many customers do we have in each segment?
SELECT
    segment,
    COUNT(*) AS total_customers
FROM customers
GROUP BY segment
ORDER BY total_customers DESC;


-- Q10. Total revenue generated by each order
SELECT
    oi.order_id,
    ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS order_revenue
FROM order_items oi
GROUP BY oi.order_id
ORDER BY order_revenue DESC;


-- Q11. Price stats for each product category
-- Shows average, lowest, and highest price per category
SELECT
    c.category_name,
    COUNT(p.product_id)          AS total_products,
    ROUND(AVG(p.unit_price), 2)  AS avg_price,
    MIN(p.unit_price)            AS min_price,
    MAX(p.unit_price)            AS max_price
FROM products p
JOIN categories c ON p.category_id = c.category_id
GROUP BY c.category_name
ORDER BY avg_price DESC;


-- Q12. Which months had revenue above 100,000?
SELECT
    DATE_FORMAT(o.order_date, '%Y-%m') AS order_month,
    ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS monthly_revenue
FROM orders o
JOIN order_items oi ON o.order_id = oi.order_id
WHERE o.status != 'Cancelled'
GROUP BY order_month
HAVING monthly_revenue > 100000
ORDER BY order_month;


-- Q13. How many orders has each employee handled?
SELECT
    e.employee_id,
    CONCAT(e.first_name, ' ', e.last_name) AS employee_name,
    COUNT(o.order_id) AS orders_handled
FROM employees e
LEFT JOIN orders o ON e.employee_id = o.employee_id
GROUP BY e.employee_id, employee_name
ORDER BY orders_handled DESC;


