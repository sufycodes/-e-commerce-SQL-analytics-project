-- SECTION 8: Window Functions

-- Q25. Number each customer within their city, ordered by signup date
-- ROW_NUMBER restarts from 1 for every new city
SELECT
    ROW_NUMBER() OVER (PARTITION BY city ORDER BY signup_date) AS row_num,
    customer_id,
    CONCAT(first_name, ' ', last_name) AS customer_name,
    city,
    signup_date
FROM customers;


-- Q26. Rank products by revenue within each category
-- RANK skips numbers after a tie, DENSE_RANK does not
SELECT
    category_name,
    product_name,
    total_revenue,
    RANK()       OVER (PARTITION BY category_name ORDER BY total_revenue DESC) AS revenue_rank,
    DENSE_RANK() OVER (PARTITION BY category_name ORDER BY total_revenue DESC) AS dense_rnk
FROM vw_product_performance
ORDER BY category_name, revenue_rank;


-- Q27. Split customers into 4 equal spending groups (quartiles)
-- Quartile 1 = top spenders, quartile 4 = lowest spenders
SELECT
    customer_name,
    total_spent,
    NTILE(4) OVER (ORDER BY total_spent DESC) AS spending_quartile
FROM (
    SELECT
        CONCAT(cu.first_name, ' ', cu.last_name) AS customer_name,
        ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS total_spent
    FROM customers cu
    JOIN orders      o  ON cu.customer_id = o.customer_id
    JOIN order_items oi ON o.order_id     = oi.order_id
    WHERE o.status != 'Cancelled'
    GROUP BY cu.customer_id, customer_name
) AS spending
ORDER BY spending_quartile, total_spent DESC;


-- Q28. Running total of revenue across all delivered orders over time
SELECT
    order_id,
    order_date,
    order_revenue,
    ROUND(
        SUM(order_revenue) OVER (ORDER BY order_date ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW),
    2) AS running_total
FROM vw_order_summary
WHERE status = 'Delivered'
ORDER BY order_date;


-- Q29. 3-order rolling average to smooth out revenue spikes
SELECT
    order_id,
    order_date,
    order_revenue,
    ROUND(AVG(order_revenue) OVER (
        ORDER BY order_date
        ROWS BETWEEN 2 PRECEDING AND CURRENT ROW
    ), 2) AS rolling_3_avg
FROM vw_order_summary
WHERE status = 'Delivered'
ORDER BY order_date;


-- Q30. Compare each order's revenue to the one before and after it
-- Useful for spotting sudden revenue jumps or drops
SELECT
    order_id,
    order_date,
    order_revenue,
    LAG(order_revenue,  1) OVER (ORDER BY order_date) AS prev_order_revenue,
    LEAD(order_revenue, 1) OVER (ORDER BY order_date) AS next_order_revenue,
    ROUND(order_revenue - LAG(order_revenue, 1) OVER (ORDER BY order_date), 2) AS revenue_change
FROM vw_order_summary
WHERE status = 'Delivered'
ORDER BY order_date;


-- Q31. Month-over-month revenue change, broken down by region
-- LAG is partitioned so each region compares against its own previous month
WITH monthly_revenue AS (
    SELECT
        region_name,
        DATE_FORMAT(order_date, '%Y-%m') AS order_month,
        ROUND(SUM(order_revenue), 2)     AS monthly_rev
    FROM vw_order_summary
    WHERE status = 'Delivered'
    GROUP BY region_name, order_month
)
SELECT
    region_name,
    order_month,
    monthly_rev,
    LAG(monthly_rev) OVER (PARTITION BY region_name ORDER BY order_month) AS prev_month_rev,
    ROUND(
        monthly_rev - LAG(monthly_rev) OVER (PARTITION BY region_name ORDER BY order_month),
    2) AS mom_change
FROM monthly_revenue
ORDER BY region_name, order_month;


-- Q32. For each customer, find their very first and most recent order
-- We use FIRST_VALUE twice: once sorted oldest-first, once newest-first
SELECT DISTINCT
    customer_name,
    FIRST_VALUE(order_id)   OVER (PARTITION BY customer_id ORDER BY order_date)      AS first_order_id,
    FIRST_VALUE(order_date) OVER (PARTITION BY customer_id ORDER BY order_date)      AS first_order_date,
    FIRST_VALUE(order_id)   OVER (PARTITION BY customer_id ORDER BY order_date DESC) AS last_order_id,
    FIRST_VALUE(order_date) OVER (PARTITION BY customer_id ORDER BY order_date DESC) AS last_order_date
FROM vw_order_summary
ORDER BY customer_name;



-- SECTION 9: CTEs (Common Table Expressions)

-- Q33. Calculate how much each customer has spent, then assign them a tier
-- The CTE handles the math, the outer query handles the labelling
WITH customer_spending AS (
    SELECT
        cu.customer_id,
        CONCAT(cu.first_name, ' ', cu.last_name) AS customer_name,
        cu.segment,
        ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS total_spent
    FROM customers cu
    JOIN orders      o  ON cu.customer_id = o.customer_id
    JOIN order_items oi ON o.order_id     = oi.order_id
    WHERE o.status != 'Cancelled'
    GROUP BY cu.customer_id, customer_name, cu.segment
)
SELECT
    customer_name,
    segment,
    total_spent,
    CASE
        WHEN total_spent >= 200000 THEN 'Platinum'
        WHEN total_spent >= 100000 THEN 'Gold'
        WHEN total_spent >= 30000  THEN 'Silver'
        ELSE 'Bronze'
    END AS value_tier
FROM customer_spending
ORDER BY total_spent DESC;


-- Q34. What percentage of total revenue does each category contribute?
-- Two CTEs are chained: first we get revenue per category, then the grand total
WITH category_rev AS (
    SELECT
        cat.category_name,
        ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS cat_revenue
    FROM order_items oi
    JOIN products    p   ON oi.product_id = p.product_id
    JOIN categories  cat ON p.category_id = cat.category_id
    JOIN orders      o   ON oi.order_id   = o.order_id
    WHERE o.status != 'Cancelled'
    GROUP BY cat.category_name
),
total_rev AS (
    SELECT SUM(cat_revenue) AS grand_total FROM category_rev
)
SELECT
    cr.category_name,
    cr.cat_revenue,
    ROUND(cr.cat_revenue / tr.grand_total * 100, 2) AS revenue_share_pct
FROM category_rev cr
CROSS JOIN total_rev tr
ORDER BY revenue_share_pct DESC;


-- Q35. Build the full employee hierarchy from top manager down to each staff member
-- The recursive CTE starts at the top (no manager) and works its way down level by level
WITH RECURSIVE org_chart AS (

    -- Start with top-level employees who have no manager above them
    SELECT
        employee_id,
        CONCAT(first_name, ' ', last_name)          AS employee_name,
        job_title,
        manager_id,
        0                                            AS level,
        CAST(CONCAT(first_name, ' ', last_name) AS CHAR(500)) AS hierarchy_path
    FROM employees
    WHERE manager_id IS NULL

    UNION ALL

    -- For each employee found, find who reports to them
    SELECT
        e.employee_id,
        CONCAT(e.first_name, ' ', e.last_name),
        e.job_title,
        e.manager_id,
        oc.level + 1,
        CONCAT(oc.hierarchy_path, ' > ', e.first_name, ' ', e.last_name)
    FROM employees e
    JOIN org_chart oc ON e.manager_id = oc.employee_id
)
SELECT
    REPEAT('    ', level) AS indent,
    employee_name,
    job_title,
    level,
    hierarchy_path
FROM org_chart
ORDER BY hierarchy_path;



-- SECTION 10: Stored Procedures and Functions


DELIMITER $$

-- Procedure 1: Pass in a customer ID and get back all their orders with totals
CREATE PROCEDURE sp_get_customer_orders(IN p_customer_id INT)
BEGIN
    SELECT
        o.order_id,
        o.order_date,
        o.status,
        ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS order_total
    FROM orders o
    JOIN order_items oi ON o.order_id = oi.order_id
    WHERE o.customer_id = p_customer_id
    GROUP BY o.order_id, o.order_date, o.status
    ORDER BY o.order_date DESC;
END$$


-- Procedure 2: Reduce prices for all active products in a given category
-- Returns how many products were updated via the OUT parameter
CREATE PROCEDURE sp_apply_category_discount(
    IN  p_category_id      INT,
    IN  p_discount_pct     DECIMAL(4,2),
    OUT p_products_updated INT
)
BEGIN
    UPDATE products
    SET unit_price = ROUND(unit_price * (1 - p_discount_pct / 100), 2)
    WHERE category_id = p_category_id
      AND is_active = 1;

    SET p_products_updated = ROW_COUNT();
END$$


-- Function: Give it an order ID, it returns the net revenue for that order
CREATE FUNCTION fn_order_revenue(p_order_id INT)
RETURNS DECIMAL(12,2)
DETERMINISTIC
READS SQL DATA
BEGIN
    DECLARE v_revenue DECIMAL(12,2);

    SELECT ROUND(SUM(quantity * unit_price * (1 - discount_pct)), 2)
    INTO v_revenue
    FROM order_items
    WHERE order_id = p_order_id;

    RETURN COALESCE(v_revenue, 0);
END$$

DELIMITER ;


