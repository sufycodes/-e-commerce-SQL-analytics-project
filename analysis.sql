-- Q6. Calculate the revenue for each line item in an order
SELECT
    order_id,
    product_id,
    quantity,
    unit_price,
    discount_pct,
    ROUND(quantity * unit_price * (1 - discount_pct), 2) AS line_revenue
FROM order_items
ORDER BY order_id;


-- Q7. Find all products that have "Set" in their name
