-- Q22. Assign each customer a value tier based on how much they've spent
-- Tiers go from Bronze (lowest) up to Platinum (highest)
SELECT
    CONCAT(cu.first_name, ' ', cu.last_name) AS customer_name,
    ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS total_spent,
    CASE
        WHEN SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)) >= 200000 THEN 'Platinum'
        WHEN SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)) >= 100000 THEN 'Gold'
        WHEN SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)) >= 30000  THEN 'Silver'
        ELSE 'Bronze'
    END AS value_tier
FROM customers cu
JOIN orders      o  ON cu.customer_id = o.customer_id
JOIN order_items oi ON o.order_id     = oi.order_id
WHERE o.status != 'Cancelled'
GROUP BY cu.customer_id, customer_name
ORDER BY total_spent DESC;


-- Q23. For each employee, count how many orders ended up in each status
SELECT
    CONCAT(e.first_name, ' ', e.last_name) AS employee_name,
    COUNT(o.order_id)                                          AS total_orders,
    SUM(CASE WHEN o.status = 'Delivered'  THEN 1 ELSE 0 END)  AS delivered,
    SUM(CASE WHEN o.status = 'Cancelled'  THEN 1 ELSE 0 END)  AS cancelled,
    SUM(CASE WHEN o.status = 'Processing' THEN 1 ELSE 0 END)  AS processing,
    SUM(CASE WHEN o.status = 'Pending'    THEN 1 ELSE 0 END)  AS pending
FROM employees e
LEFT JOIN orders o ON e.employee_id = o.employee_id
GROUP BY e.employee_id, employee_name
ORDER BY total_orders DESC;


-- Q24. Products that cost more than the average price in their own category
SELECT
    p.product_name,
    p.unit_price,
    c.category_name,
    ROUND((
        SELECT AVG(p2.unit_price)
        FROM products p2
        WHERE p2.category_id = p.category_id
    ), 2) AS category_avg_price
FROM products p
JOIN categories c ON p.category_id = c.category_id
WHERE p.unit_price > (
    SELECT AVG(p3.unit_price)
    FROM products p3
    WHERE p3.category_id = p.category_id
)
ORDER BY c.category_name, p.unit_price DESC;


-- SECTION 7: Views


-- View 1: A reusable summary of every order with customer, employee, region, and revenue
-- We use this view in window function and CTE queries below
CREATE OR REPLACE VIEW vw_order_summary AS
SELECT
    o.order_id,
    o.order_date,
    o.status,
    cu.customer_id,
    CONCAT(cu.first_name, ' ', cu.last_name) AS customer_name,
    cu.segment,
    cu.city,
    CONCAT(e.first_name, ' ', e.last_name)   AS employee_name,
    r.region_name,
    ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2) AS order_revenue
FROM orders o
JOIN customers      cu ON o.customer_id  = cu.customer_id
JOIN order_items    oi ON o.order_id     = oi.order_id
LEFT JOIN employees  e ON o.employee_id  = e.employee_id
LEFT JOIN sales_regions r ON e.region_id = r.region_id
GROUP BY
    o.order_id, o.order_date, o.status,
    cu.customer_id, customer_name, cu.segment, cu.city,
    employee_name, r.region_name;


-- View 2: How each product is performing in terms of units sold and total revenue
CREATE OR REPLACE VIEW vw_product_performance AS
SELECT
    p.product_id,
    p.product_name,
    c.category_name,
    p.unit_price,
    p.stock_qty,
    COALESCE(SUM(oi.quantity), 0)                                             AS total_units_sold,
    COALESCE(ROUND(SUM(oi.quantity * oi.unit_price * (1 - oi.discount_pct)), 2), 0) AS total_revenue
FROM products p
JOIN categories  c  ON p.category_id  = c.category_id
LEFT JOIN order_items oi ON p.product_id = oi.product_id
LEFT JOIN orders       o ON oi.order_id  = o.order_id AND o.status != 'Cancelled'
GROUP BY p.product_id, p.product_name, c.category_name, p.unit_price, p.stock_qty;


