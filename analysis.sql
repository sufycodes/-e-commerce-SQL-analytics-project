-- Basic Queries

-- Q1. Show all customers, newest signups first
SELECT
    customer_id,
    CONCAT(first_name, ' ', last_name) AS full_name,
    signup_date
FROM customers
ORDER BY signup_date DESC;


-- Q2. All orders that were delivered in 2023
SELECT
    order_id,
    customer_id,
    order_date,
    shipped_date,
    status
FROM orders
WHERE status = 'Delivered'
  AND order_date BETWEEN '2023-01-01' AND '2023-12-31'
ORDER BY order_date;


-- Q3. Products priced between 5,000 and 20,000 (cheapest first)
SELECT
    product_name,
    unit_price,
    stock_qty
FROM products
WHERE unit_price BETWEEN 5000 AND 20000
ORDER BY unit_price ASC;


-- Q4. Customers living in Karachi or Lahore
SELECT
    CONCAT(first_name, ' ', last_name) AS full_name,
    city,
    email
FROM customers
