-- Q18. Full order breakdown: customer, product, category, and line total
SELECT
    o.order_id,
    CONCAT(cu.first_name, ' ', cu.last_name) AS customer,
    p.product_name,
    cat.category_name,
    oi.quantity,
    oi.unit_price,
